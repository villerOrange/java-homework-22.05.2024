package org.example.hw_22_05_2024;

public class Concatenation {
    public static void main(String[] args) {
        // Создание двух строковых переменных
        String partOne = "Hello, ";
        String partTwo = "world!";

        // Конкатенация строк
        String result = partOne + partTwo;
        System.out.println("Результат конкатенации: " + result);
    }
}
